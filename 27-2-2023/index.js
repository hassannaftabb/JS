sum(1000000, 382947823);

//custom function
function isGreaterThan30(num) {
  if (num * 10 > 30) {
    console.log("Hellow");
  } else {
    console.log("Bye!");
  }
}

//calling custom fucntion
isGreaterThan30(4);
