//Loops
//Each run of a loop is called an iteration.

var personInClass = ["Hammad", "Hassan", "Mishal", "Ayesha"];

for (let iteration = 0; iteration < personInClass.length; iteration++) {
  console.log(personInClass[iteration]);
}

